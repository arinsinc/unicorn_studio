package com.unicorn.studio.rest;

import org.springframework.http.HttpStatus;
import org.springframework.http.ResponseEntity;
import org.springframework.web.bind.annotation.ControllerAdvice;
import org.springframework.web.bind.annotation.ExceptionHandler;

@ControllerAdvice
public class APIExceptionHandler {
	// Add exception handlers using @ExceptionHandler 
	@ExceptionHandler
	public ResponseEntity<APIErrorResponse> handleException(NotFoundException exc) {
		APIErrorResponse error = new APIErrorResponse();
		error.setStatus(HttpStatus.NOT_FOUND.value());
		error.setMessage(exc.getMessage());
		error.setTimeStamp(System.currentTimeMillis());
		return new ResponseEntity<>(error, HttpStatus.NOT_FOUND);
	}
		
	@ExceptionHandler
	public ResponseEntity<APIErrorResponse> handleResponse(Exception exc) {
		APIErrorResponse error = new APIErrorResponse();
		error.setStatus(HttpStatus.BAD_REQUEST.value());
		error.setMessage(exc.getMessage());
		error.setTimeStamp(System.currentTimeMillis());
		return new ResponseEntity<>(error, HttpStatus.BAD_REQUEST);
	}
}
